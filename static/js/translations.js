// ── Moteur de traduction ──────────────────────────────────────
const I18N = {
  currentLang: localStorage.getItem('mydoctor_lang') || 'fr',

  t(key) {
    const lang = this.currentLang;
    return (TRANSLATIONS[lang] && TRANSLATIONS[lang][key])
      ? TRANSLATIONS[lang][key]
      : (TRANSLATIONS['fr'][key] || key);
  },

  setLang(lang) {
    this.currentLang = lang;
    localStorage.setItem('mydoctor_lang', lang);
    this.applyAll();
    // RTL pour l'arabe
    if (lang === 'ar') {
      document.documentElement.setAttribute('dir', 'rtl');
      document.documentElement.setAttribute('lang', 'ar');
      document.body.style.fontFamily = "'Cairo', sans-serif";
    } else {
      document.documentElement.setAttribute('dir', 'ltr');
      document.documentElement.setAttribute('lang', lang);
      document.body.style.fontFamily = "";
    }
    // Mettre à jour les boutons actifs
    document.querySelectorAll('.btn-lang').forEach(btn => {
      btn.classList.toggle('btn-primary', btn.dataset.lang === lang);
      btn.classList.toggle('btn-outline-light', btn.dataset.lang !== lang);
    });
  },

  applyAll() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const val = this.t(key);
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = val;
      } else if (el.tagName === 'OPTION') {
        // ne pas traduire les options dynamiques
      } else {
        el.textContent = val;
      }
    });
  },

  init() {
    this.applyAll();
    this.setLang(this.currentLang);
  }
};

document.addEventListener('DOMContentLoaded', () => I18N.init());

